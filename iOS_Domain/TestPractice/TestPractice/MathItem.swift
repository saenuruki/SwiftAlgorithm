import Foundation

class MathItem {
    
    func addNumbers(_ a: Int, _ b: Int) -> Int {
        return a + b
    }
    
    func multiplyNumbers(_ a: Int, _ b: Int) -> Int {
        return a * b
    }
    
    func isSameNumber(_ a: Int, _ b: Int) -> Bool {
        return a == b
    }
}
